package kr.happyjob.study.info.model;

public class EngineerInfoModel{

	private String engineer_no;
	private String engineer_name;
	private String engineer_area;
	private String engineer_grade;
	private String engineer_skill;
	private String user_contents;
	
	public String getEngineer_no() {
		return engineer_no;
	}
	public void setEngineer_no(String engineer_no) {
		this.engineer_no = engineer_no;
	}
	public String getEngineer_name() {
		return engineer_name;
	}
	public void setEngineer_name(String engineer_name) {
		this.engineer_name = engineer_name;
	}
	public String getEngineer_area() {
		return engineer_area;
	}
	public void setEngineer_area(String engineer_area) {
		this.engineer_area = engineer_area;
	}
	public String getEngineer_grade() {
		return engineer_grade;
	}
	public void setEngineer_grade(String engineer_grade) {
		this.engineer_grade = engineer_grade;
	}
	public String getEngineer_skill() {
		return engineer_skill;
	}
	public void setEngineer_skill(String engineer_skill) {
		this.engineer_skill = engineer_skill;
	}
	public String getUser_contents() {
		return user_contents;
	}
	public void setUser_contents(String user_contents) {
		this.user_contents = user_contents;
	}
	
}
